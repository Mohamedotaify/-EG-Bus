import math
def AreaCircule(radius):
    Area = math.pi * radius * radius
    print(Area)

def AreaSquare(width):
    Area = width * width
    print(Area)

def AreaRectangle(height, width):
    Area = height * width
    print(Area)

def AreaTriangle(width, height):
    Area = 0.5 * width * height
    print(Area)